package user;

import java.sql.Connection;
import java.sql.DriverManager;
import java.sql.PreparedStatement;
import java.sql.ResultSet;

public class UserDAO { //실제로 데이터베이스에 접근해서 데이터를 넣거나 가져오는 역할을 하는 데이터접근객체
	private Connection conn;
	private PreparedStatement pstmt;
	private ResultSet rs; //데이터를 담을 객체
	
	
	
	
	//실제로 mysql에 접속하게 해주는 부분
	public UserDAO() {
		try {
			String dbURL = "jdbc:mysql://localhost:3306/BBS";
			String dbID = "root";
			String dbPassword = "s2158334";
			Class.forName("com.mysql.jdbc.Driver"); //드라이버는 sql에 접속할 수 있게 해주는 라이브러리
			conn = DriverManager.getConnection(dbURL, dbID, dbPassword);
		} catch (Exception e) {
			e.printStackTrace();
		}
		
	} 
	
	
	//한 계정에 대해 로그인 시도하는 코드
	public int login(String userID, String userPassword) {
		String SQL = "SELECT userPassword FROM USER WHERE userID = ?" ; //?로 넣은 부분은 userID로 입력받게함
		try {
			pstmt = conn.prepareStatement(SQL); // pstmt에 정해진 SQL문장을 데이터베이스에 삽입하는 방식으로 인스턴스를 가져옴
			pstmt.setString(1, userID);
			rs = pstmt.executeQuery();
			if (rs.next() ) {
				if(rs.getString(1).equals(userPassword)) {
					return 1; //로그인 성공
				}
				else
					return 0; //비밀번호 불일치
				
			}
			return -1; //아이디가 없음
		} catch (Exception e) {
			e.printStackTrace();
		}
		return -2; //데이터베이스 오류
	}
	
	
	//회원가입 기능
	public int join(User user) {
		
		String SQL = "INSERT INTO USER VALUES (?, ?, ?, ?, ?)";
		try {
			pstmt = conn.prepareStatement(SQL);
			pstmt.setString(1, user.getUserID());
			pstmt.setString(2, user.getUserPassword());
			pstmt.setString(3, user.getUserGender());
			pstmt.setString(4, user.getUserEmail());
			pstmt.setString(5, user.getUserName());
			return pstmt.executeUpdate();
			
		}catch(Exception e) {
			e.printStackTrace();
		}
		return -1; //데이터베이스 오류
	}
	
	
}












